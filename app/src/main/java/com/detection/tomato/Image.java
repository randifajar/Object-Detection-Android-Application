package com.detection.tomato;

public class Image {
    private String name, url;
    public Image(){
    }

    public String getName() {
        return name;
    }
    public String getURL() {
        return url;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setURL(String url) {
        this.url = url;
    }
}
